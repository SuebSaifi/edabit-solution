
def sum_neg(arr)
	arr1=[]
    temp=0
    sum_neg =0
    arr.each do |x|
        if x>0
            temp +=1
        else
            sum_neg += x*(-1)
        end 
    end
    arr1<< temp
    arr1 << sum_neg*-1
    p arr1
end
